print("Digite três valores, o resultado será a soma do quadrado desses valores")
num1 = float(input("Digite o primeiro valor: "))
num2 = float(input("Digite o segundo valor: "))
num3 = float(input("Agora digite o terceiro valor: "))

result = num1**2 + num2**2 + num3**2

print("O resultado da soma do quadrado desses valores é: ", "%.2f" % result)
